    /**
     * 獲取每年的時間戳
     *
     * @return array
     */
    public static function getYearStamps()
    {
        $stamp = 444495600;
        $day_seconds = 24 * 3600;
        $yStamps = [];
        foreach ( self::MonthDays as $y => $days ) {
            $yStamps[$y] = $stamp;
            $yDay = array_sum($days) - $days[0];
            $stamp += $yDay * $day_seconds;
        }
        return $yStamps;
    }
